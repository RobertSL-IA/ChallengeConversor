package Modelos;

public class Tasas {
    private double ARS;  // Peso argentino
    private double BRL;  // Real brasileño
    private double COP;  // Peso colombiano
    private double USD;  // Dólar estadounidense
    private double CLP;  // Peso chileno
    private double MXN;  // Peso mexicano
    private double PEN;  // Sol peruano
    private double UYU;  // Peso uruguayo

    // Getters y Setters
    public double getARS() {
        return ARS;
    }

    public void setARS(double ARS) {
        this.ARS = ARS;
    }

    public double getBRL() {
        return BRL;
    }

    public void setBRL(double BRL) {
        this.BRL = BRL;
    }

    public double getCOP() {
        return COP;
    }

    public void setCOP(double COP) {
        this.COP = COP;
    }

    public double getUSD() {
        return USD;
    }

    public void setUSD(double USD) {
        this.USD = USD;
    }

    public double getCLP() {
        return CLP;
    }

    public void setCLP(double CLP) {
        this.CLP = CLP;
    }

    public double getMXN() {
        return MXN;
    }

    public void setMXN(double MXN) {
        this.MXN = MXN;
    }

    public double getPEN() {
        return PEN;
    }

    public void setPEN(double PEN) {
        this.PEN = PEN;
    }

    public double getUYU() {
        return UYU;
    }

    public void setUYU(double UYU) {
        this.UYU = UYU;
    }
}

